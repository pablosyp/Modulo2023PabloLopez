    private void Envio(){
        String dato = "";
        
        try{
        FileReader leer = new FileReader(textM.getText()+".txt");
        BufferedReader contenedor = new BufferedReader(leer);
        while ((dato = (contenedor.readLine())) !=null){
            System.out.println(dato);
        }
        }catch (Exception err){
            System.out.println(err);
        }
        
    } 
    private void Crear(){
        //Metodo para crear el archivo
        String archivo = textM.getText() + ".txt";
        
        //Verificamos el nombre del archivo
        if(textM.getText().equals("")){
            JOptionPane.showMessageDialog(null,"No hay Info");
        }else{
            try {
                //Escribimos el archivo
                FileWriter escribir = new FileWriter(archivo,true);
                escribir.write("Nombre:"+ textM.getText() + "\r\n");
                //escribir.close();
                JOptionPane.showOptionDialog(null,
                "Su cambio de contra√±a se a enviado a :"+textM.getText(),
                "Datos de Usuario",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,null,
                new Object[]{"SALIR"},"");
                
            } catch (IOException ex) {
                System.out.println(ex);
            }
            
            
        }
        
        
    }
